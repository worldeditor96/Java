
public class ConvertorAtoR {
	String convertNum;

	public String conAtoR(int str) {
		switch (str) {
		case 1:
			convertNum = "I";
			break;
		case 2:
			convertNum = "II";
			break;
		case 3:
			convertNum = "III";
			break;
		case 4:
			convertNum = "IV";
			break;
		case 5:
			convertNum = "V";
			break;
		case 6:
			convertNum = "VI";
			break;
		case 7:
			convertNum = "VII";
			break;
		case 8:
			convertNum = "VIII";
			break;
		case 9:
			convertNum = "IX";
			break;
		case 10:
			convertNum = "X";
			break;
		case 11:
			convertNum = "XI";
			break;
		case 12:
			convertNum = "XII";
			break;
		case 13:
			convertNum = "XIII";
			break;
		case 14:
			convertNum = "XIV";
			break;
		case 15:
			convertNum = "XV";
			break;
		case 16:
			convertNum = "XVI";
			break;
		case 17:
			convertNum = "XVII";
			break;
		case 18:
			convertNum = "XVIII";
			break;
		case 19:
			convertNum = "XIX";
			break;
		case 20:
			convertNum = "XX";
			break;
		default:
			
		}
		return convertNum;

	}
}
