
public class ConvertorRtoA {
	int convertNum;

	public int con(String str) {
		switch (str) {
		case "I":
			convertNum = 1;
			break;
		case "II":
			convertNum = 2;
			break;
		case "III":
			convertNum = 3;
			break;
		case "IV":
			convertNum = 4;
			break;
		case "V":
			convertNum = 5;
			break;
		case "VI":
			convertNum = 6;
			break;
		case "VII":
			convertNum = 7;
			break;
		case "VIII":
			convertNum = 8;
			break;
		case "IX":
			convertNum = 9;
			break;
		case "X":
			convertNum = 10;
			break;

		default:
			
		}
		return convertNum;

	}
}
